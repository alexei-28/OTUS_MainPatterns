package ru.otus.main_patterns.hw10;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

/*
    Домашнее задание:
    Создать микросервис Авторизация пользователя

    Цель:
    В одном из предыдущих ДЗ необходимо было реализовать Endpoint на Игровом сервере для приема входящих сообщений от Агента.

    Термины:
    Игровой сервер - это приложение, на котором вычисляется состояние космических боев, то есть выполняются все те команды,
    которые рассматривались на предыдущих занятиях.
    Агент - это специальное приложение, на котором игрок запускает свой алгоритм управления своей командой космических кораблей.
    При реализации этого Endpoint возникает задача авторизации пользователя на отправку сообщений для управления командой
    танков в конкретной игре, чтобы не допустить вмешательства в процесс игры сторонними пользователями.
    В результате выполнения этого ДЗ будет разработан микросервис, который выдает jwt токен из участников танкового сражения,
    для того, чтобы игровой сервер мог принять решение о возможности выполнения входящего сообщения от имени пользователя.

    Цель: применить навыки разработки микросервиса.
    Описание/Пошаговая инструкция выполнения домашнего задания:
    Предполагается реализация микросервиса авторизации с помощью jwt токена.

    Алгоритм взаимодействия сервиса авторизации и Игрового сервера следующий:
    1. Один из пользователей организует космический бой и определяет список участников (их может быть больше 2-х).
        На сервер авторизации уходит запрос о том, что организуется космический бой и присылается список его участников.
        Сервер в ответ возвращает id космического боя.
    2. Аутентифицированный пользователь посылает запрос на выдачу jwt токена, который авторизует право этого пользователя на участие в игре.
        Для этого он должен указать в запросе id игры.
        Если пользователь был указан в списке участников игры, то он выдает пользователю jwt токен, в котором указан id игры.
    3. Пользователь при отправке сообщений в Игровой сервер прикрепляет к сообщениям выданный jwt токен,
        чтобы сервер при получении сообщения удостоверяется, что токен был выдан сервером авторизации (проверят хэш jwt токена)
        и проверяет, что пользователь запросил выполнение операции в игре, в которой он эту операцию может выполнять.

    Критерии оценки:
    1. Задача сдана на проверку - 1 балл.
    2. Код компилируется без ошибок, тесты выполняются успешно. - 1 балл
    3. Настроен CI, по которому можно определить успешность п.2 - 2 балла
    4. Реализована аутентификация с помощью jwt (выдачу токенов выполняет сервис авторизации)- 2 балла
    5. Реализован запрос на сервис авторизации для создания нового космического боя - 2 балла
    6. Реализована проверка jwt во входящем сообщении на Игровом сервере - 2 балла

    Максимальная оценка за задание 10 баллов.
    Задание принимается, если было оценено не менее, чем в 7 баллов.

    Решение:
    1. AuthServer - отдельный микросервис (порт 8081) - выдаёт JWT
    2. GameServer - отдельный микросервис (порт 8080) - валидирует JWT и выполняет команду
    3. YAML парсится вручную (очень упрощённо)
    4. JWT реализован вручную (HMAC-SHA256)

    Архитектура:
    Client (POST)
      ↓ (login + password hash)
    AuthServer
      ↓ (JWT)
    Client (POST)
      ↓ (JWT + command)
    GameServer
      ↓
    Validate JWT → Execute command

    Алгоритм работы (на примере команды MOVE_STRAIGHT):
    1. Запускаем микросервис AuthServer(сервис авторизации) на порту 8081
    2. Выполняем REST запрос к AuthServer. Пароль хэшируем с помощью HMAC-SHA256
       HTTP POST http://localhost:8081/auth/login

       {
         "username":"player-1",
         "passwordHash":"player-1-pass"
       }
    3. В ответ от AuthServer получаем JWT токен (время жизни 15 минут)
    4. Запускаем микросервис GameServer(Игровой сервер) на порту 8080
       Список игроков, который имеют права запуска команд - resources/game-config.yml
    5. Выполняем REST запрос к GameServer на выполнение команды. В заголовке передаём JWT токен.
       HTTP POST http://localhost:8080/game/order
       Authorization Bearer VALID_JWT_TOKEN

        {
          "id" : "c4370147-695f-4a43-aea1-9fb5d6fb5da1",
          "gameId": "game-1",
          "playerId": "player-1",
          "operationId": "MOVE_STRAIGHT",
          "operationArgs": {
            "initialVelocity": 2
          }
        }

      6. В случае валидного JWT токена, команда будет выполнена (статус 201)
         {
           "orderId": "c4370147-695f-4a43-aea1-9fb5d6fb5da1",
           "message": "Operation 'MOVE_STRAIGHT' successfully created"
         }

       7. В случае невалидной подписи JWT токена, команда не будет выполнена (статус 401)
         {
           "message": "Token invalid signature"
         }
*/
public class GameServerMain {
  private static final Logger logger = LoggerFactory.getLogger(GameServerMain.class);

  public static void main(String[] args) throws Exception {
    logger.info(
        "Java version: {}, Java vendor: {}\nДомашнее задание#10: Создать микросервис Авторизация пользователя",
        System.getProperty("java.version"),
        System.getProperty("java.vendor"));
    GameServer gameServer = new GameServer(8080);
    gameServer.start();
    logger.debug("Server started on http://localhost:8080/users");
    gameServer.join();
  }
}
